      PROGRAM TTEST
      REAL AOS(10)
      REAL LOS(10)
      INTEGER NREVS
      REAL DIFF(10), AVG
      CALL RDAOSLOS (LOS, AOS, NREVS)
      WRITE(6,801) NREVS
 801  FORMAT(I2,' AOS/LOS RECORDS RECEIVED')
      WRITE(6,899)
      WRITE(6,802) 'LOST (HRS)', 'ACQRD (HRS)', 'ELAPSED (SEC)'
      WRITE(6,802) '---------------', '-------------', '---------------'
 802  FORMAT(A15,1X,A15,1X,A15)
      DO 100 I=1,NREVS
         DIFF(I)=DIFFSECS(LOS(I),AOS(I))
         WRITE(6,803) LOS(I), AOS(I), DIFF(I)
 803     FORMAT(F15.10,1X,F15.10,1X,F15.2)
 100  CONTINUE
      WRITE(6,899)
      AVG=XMEAN(DIFF,NREVS)
      WRITE(6,804) AVG, XSTDDEV(DIFF,NREVS,AVG) 
 804  FORMAT(' AVG SIGNAL LOSS INTERVAL ',F7.2,', SIGMA ', F7.1)
 899  FORMAT(1X)
      STOP
      END
